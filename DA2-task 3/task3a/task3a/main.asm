BEGIN: 
	LDI R20, 0xff
	OUT OCR0A,R20 
	LDI R20,(1<<WGM01)
	OUT TCCR0A,R20 
	LDI R20,5;
	OUT TCCR0B, R20
AGAIN: 
	IN R20,TIFR0 
