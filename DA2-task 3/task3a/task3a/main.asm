	RJMP AGAIN
	DEC R18
	SBRS R18,0
	RJMP BEGIN
	LDI R20,0x0
	OUT TCCR0B,R20 
	LDI R20,(1<<OCF0A)
	OUT TIFR0,R20 
	EOR R17,R16 
	OUT PORTB,R17 
	LDI R18,0xff
	RJMP BEGIN